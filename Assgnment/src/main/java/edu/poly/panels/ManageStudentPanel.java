    private void initTableModel(){
        String[] columnHeaders = new String[]{
            "Mã SV", "Họ Tên", "Email", "Số ĐT", "Giới Tính", "Địa Chỉ", "Hình"
        };
        tblModel = new DefaultTableModel();
        tblModel.setColumnIdentifiers(columnHeaders);
        tblStudents.setModel(tblModel);
    }
    private void loadTableData(){
        try {
            while (tblModel.getRowCount()>0){
                tblModel.removeRow(0);
            }
            
            StudentDao dao = new StudentDao();
            List<Student> list = dao.findStudents();
            for (Student student : list) {
                Vector row = new Vector();
                row.add(student.getMaSV());
                row.add(student.getHoTen());
                row.add(student.getEmail());
                row.add(student.getSoDT());
                row.add(student.isGioiTinh());
                row.add(student.getDiaChi());
                row.add(student.getHinh());
                tblModel.addRow(row);
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }


    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            StringBuilder sb = new StringBuilder();
            if (txtHoTen.getText().equals("")){
                sb.append("Vui lòng nhập Họ tên");
                txtHoTen.setBackground(Color.red);
            }else{
                txtHoTen.setBackground(Color.white);
            }
            if (sb.length()> 0){
                JOptionPane.showMessageDialog(this, sb.toString());
                return;
            }
            
            StudentDao dao = new StudentDao();
            Student student = new Student();
            student.setMaSV(txtMaSV.getText());
            student.setHoTen(txtHoTen.getText());
            student.setDiaChi(txaDiaChi.getText());
            student.setGioiTinh(rdbNam.isSelected());
            student.setEmail(txtEmail.getText());
            student.setSoDT(txtSoDT.getText());
            
            String dir = System.getProperty("user.dir");
            dir = dir + "/images";
            File file = new File(dir);
            if (!file.exists()){
                file.mkdir();
            }
            
            dir += "/" + selectedPicture.getName();
            Path copied = Paths.get(dir);
            Path originalPath = Paths.get(selectedPicture.getAbsolutePath());
            Files.copy(originalPath, copied, StandardCopyOption.REPLACE_EXISTING);
            
            student.setHinh(selectedPicture.getName());
            
            if (dao.insertStudent(student)){
                JOptionPane.showMessageDialog(this, "Sinh viên đã được lưu");
            }else{
                JOptionPane.showMessageDialog(this, "Sinh viên đã không được lưu");
            }
            loadTableData();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void tblStudentsMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStudentsMousePressed
        try {
            JTable table = (JTable) evt.getSource();
            
            if (evt.getClickCount()== 2 && table.getSelectedRow() >= 0){
                
                Object idObject = tblModel.getValueAt(table.getSelectedRow(), 0);
                StudentDao dao = new StudentDao();
                Student student = dao.findStudentById(idObject.toString());
                if (student != null){
                    txtHoTen.setText(student.getHoTen());
                    txaDiaChi.setText(student.getDiaChi());
                    txtEmail.setText(student.getEmail());
                    txtMaSV.setText(student.getMaSV());
                    txtSoDT.setText(student.getSoDT());
                    rdbNam.setSelected(student.isGioiTinh());
                    rdbNu.setSelected(!student.isGioiTinh());
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_tblStudentsMousePressed

